# Functions with parameters
# Functions can have Parameters
# num1 and num2 are now parameters
def add(num1, num2):
    # num1 = 20
    # num2 = 89
    answer = num1 + num2
    print('The addition is ', answer)
 

# Function call
# We provide the arguments
add(num1=90, num2=4)
# We can call the function again with different arguments.
add(num1=190, num2=10)





