# Functions can have Parameters
def interest(principle, rate, time):
    # num1 = 20
    # num2 = 89
    answer = principle * rate * time/100
    print('The Interest is:  ', answer)
 

# Function call
# We provide the arguments
interest(principle=300, rate=2,time=3)
# We can call the function again with different arguments.

interest(principle=4000, rate=2,time=8)
# We can call the function again with different arguments.


